DROP TABLE IF EXISTS PERSON;

CREATE TABLE PERSON
(
    ID        NUMBER PRIMARY KEY AUTO_INCREMENT,
    FIRSTNAME VARCHAR(255),
    LASTNAME  VARCHAR(255),
    BIRTHDAY  DATE,
    GENDER    VARCHAR(1)
);

